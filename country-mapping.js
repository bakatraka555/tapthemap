// Funkcija za dobijanje ISO koda dr탑ave
export function getCountryCode(properties) {
    const name = properties.name || properties.NAME_EN || properties.ADMIN || properties.NAME_LONG || properties.SOVEREIGNT || 'Unknown';
    console.log('getCountryCode called with name:', name);
    
    const isoCode = countryMapping[name] || 'UNK';
    console.log('Mapped', name, 'to', isoCode);
    
    return isoCode;
}

// Funkcija za dobijanje imena dr탑ave
export function getCountryName(properties) {
    return properties.name || properties.NAME_EN || properties.ADMIN || properties.NAME_LONG || properties.SOVEREIGNT || 'Unknown';
}
