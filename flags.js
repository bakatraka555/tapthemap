export function getCountryFlag(countryCode) {
    console.log('getCountryFlag called with:', countryCode);
    
    // Vrati SVG ako postoji, inače fallback na emoji
    if (svgFlags[countryCode]) {
        return svgFlags[countryCode];
    }
    
    const flag = emojiFlags[countryCode] || '🏳️';
    console.log('Returning flag:', flag, 'for country:', countryCode);
    return flag;
}
